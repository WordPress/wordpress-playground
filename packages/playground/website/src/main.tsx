import { createRoot } from 'react-dom/client';
import { DropdownMenu, MenuGroup } from '@wordpress/components';
import { menu } from '@wordpress/icons';
import PlaygroundViewport from './components/playground-viewport';
import css from './style.module.css';
import buttonCss from './components/button/style.module.css';
import './styles.css';

import { makeBlueprint } from './lib/make-blueprint';
import type { Blueprint } from '@wp-playground/blueprints';
import PlaygroundConfigurationGroup from './components/playground-configuration-group';
import { PlaygroundConfiguration } from './components/playground-configuration-group/form';
import { SupportedPHPVersions } from '@php-wasm/universal';
import { StorageType, StorageTypes } from './types';
import { ResetSiteMenuItem } from './components/toolbar-buttons/reset-site';
import { DownloadAsZipMenuItem } from './components/toolbar-buttons/download-as-zip';
import { RestoreFromZipMenuItem } from './components/toolbar-buttons/restore-from-zip';
import { GithubImportMenuItem } from './components/toolbar-buttons/github-import-menu-item';
import { acquireOAuthTokenIfNeeded } from './github/acquire-oauth-token-if-needed';
import { GithubImportModal } from './github/github-import-form';
import { GithubExportMenuItem } from './components/toolbar-buttons/github-export-menu-item';
import { GithubExportModal } from './github/github-export-form';
import { GitHubPointer } from './github/analyze-github-url';
import { useMemo, useState } from 'react';
import {
	ExportFormValues,
	githubPointerToExportFormValues,
} from './github/github-export-form/form';
import { basename } from '@php-wasm/util';

const query = new URL(document.location.href).searchParams;

/*
 * Support passing blueprints in the URI fragment, e.g.:
 * /#{"landingPage": "/?p=4"}
 */
const fragment = decodeURI(document.location.hash || '#').substring(1);
let blueprint: Blueprint;
try {
	blueprint = JSON.parse(fragment);
	// Allow overriding the preferred versions using query params
	// generated by the version switchers.
	if (query.get('php') || query.get('wp')) {
		if (!blueprint.preferredVersions) {
			blueprint.preferredVersions = {} as any;
		}
		blueprint.preferredVersions!.php =
			(query.get('php') as any) ||
			blueprint.preferredVersions!.php ||
			'8.0';
		blueprint.preferredVersions!.wp =
			query.get('wp') || blueprint.preferredVersions!.wp || 'latest';
	}
} catch (e) {
	const features: Blueprint['features'] = {};
	// Networking is enabled by default, so we only need to disable it
	// if the query param is explicitly set to "no".
	if (query.get('networking') === 'no') {
		features['networking'] = false;
	}
	blueprint = makeBlueprint({
		php: query.get('php') || '8.0',
		wp: query.get('wp') || 'latest',
		theme: query.get('theme') || undefined,
		features,
		plugins: query.getAll('plugin'),
		landingPage: query.get('url') || undefined,
		phpExtensionBundles: query.getAll('php-extension-bundle') || [],
	});
}

// @ts-ignore
const opfsSupported = typeof navigator?.storage?.getDirectory !== 'undefined';
let storageRaw = query.get('storage');
if (StorageTypes.includes(storageRaw as any) && !opfsSupported) {
	storageRaw = 'none';
} else if (!StorageTypes.includes(storageRaw as any)) {
	storageRaw = 'none';
}
const storage = storageRaw as StorageType;

const isSeamless = (query.get('mode') || 'browser') === 'seamless';

const currentConfiguration: PlaygroundConfiguration = {
	wp: blueprint.preferredVersions?.wp || 'latest',
	php: resolveVersion(blueprint.preferredVersions?.php, SupportedPHPVersions),
	storage: storage || 'none',
	withExtensions: blueprint.phpExtensionBundles?.[0] === 'kitchen-sink',
};

/*
 * The 6.3 release includes a caching bug where
 * registered styles aren't enqueued when they
 * should be. This isn't present in all environments
 * but it does here in the Playground. For now,
 * the fix is to define `WP_DEVELOPMENT_MODE = all`
 * to bypass the style cache.
 *
 * @see https://core.trac.wordpress.org/ticket/59056
 */
if (currentConfiguration.wp === '6.3') {
	blueprint.steps?.unshift({
		step: 'defineWpConfigConsts',
		consts: {
			WP_DEVELOPMENT_MODE: 'all',
		},
	});
}

acquireOAuthTokenIfNeeded();

function Main() {
	const [lastGitHubImportFrom, setLastGitHubImportFrom] = useState<{
		url: string;
		pointer?: GitHubPointer;
		files?: any[];
	}>({
		url: '',
	});
	const initialExportFormValues: Partial<ExportFormValues> = useMemo(() => {
		const pointer = lastGitHubImportFrom.pointer;
		if (!pointer) {
			return {};
		}
		const values = githubPointerToExportFormValues(pointer);
		return {
			...values,
			contentType: pointer.contentType,
			plugin:
				pointer.contentType === 'plugin' ? basename(pointer.path) : '',
			theme:
				pointer.contentType === 'theme' ? basename(pointer.path) : '',
		};
	}, [lastGitHubImportFrom.pointer]);

	return (
		<PlaygroundViewport
			storage={storage}
			isSeamless={isSeamless}
			blueprint={blueprint}
			toolbarButtons={[
				<PlaygroundConfigurationGroup
					key="configuration"
					initialConfiguration={currentConfiguration}
				/>,
				<DropdownMenu
					key="menu"
					icon={menu}
					label="Additional actions"
					className={css.dropdownMenu}
					toggleProps={{
						className: `${buttonCss.button} ${buttonCss.isBrowserChrome}`,
					}}
				>
					{({ onClose }) => (
						<MenuGroup>
							<ResetSiteMenuItem
								storage={currentConfiguration.storage}
								onClose={onClose}
							/>
							<DownloadAsZipMenuItem onClose={onClose} />
							<RestoreFromZipMenuItem onClose={onClose} />
							<GithubImportMenuItem onClose={onClose} />
							<GithubExportMenuItem onClose={onClose} />
						</MenuGroup>
					)}
				</DropdownMenu>,
			]}
		>
			<GithubImportModal
				onImported={(url, pointer, files) => {
					setLastGitHubImportFrom({ url, pointer, files });
				}}
			/>
			<GithubExportModal
				initialFilesBeforeChanges={lastGitHubImportFrom.files}
				initialValues={{
					repoUrl: lastGitHubImportFrom.url || '',
					...initialExportFormValues,
				}}
			/>
		</PlaygroundViewport>
	);
}

const root = createRoot(document.getElementById('root')!);
root.render(<Main />);

function resolveVersion<T>(
	version: string | undefined,
	allVersions: readonly T[],
	defaultVersion: T = allVersions[0]
): T {
	if (
		!version ||
		!allVersions.includes(version as any) ||
		version === 'latest'
	) {
		return defaultVersion;
	}
	return version as T;
}
